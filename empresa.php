        <div id="texto_empresa">O Primo’s desde o início pensa em família. Era um sonho de 02 primos que queriam ter o próprio negócio mas a única coisa que tinham em mãos era um Carrinho de Lanches. Com apenas 8 meses de um trabalho de dedicação os primos conseguiram locar sua primeira lanchonete, onde em pouco mais de 2 anos já tinham uma equipe com 10 funcionários. 
Com lanches grandes, combos especiais, sucos e açaís muito saborosos, o Primo’s se tornou cada vez mais conhecido em São José dos Campos e comportava famílias e amigos em sua estrutura todos os dias, sem contar nas filas de espera e na quantidade de pedidos delivery.
Com o apoio de amigos e clientes, o Primo’s decidiu padronizar o atendimento e tornar o negócio promissor, em uma Franquia de sucesso, tornando a marca mais forte e aumentando sua participação de mercado.
Hoje, com 4 anos de existência, a franquia PRIMO’S BURGER já inaugurou a sua operação, e seu projeto de expansão, prevê mais 3 lojas ainda em 2013.
